void solve()
{
    input(n);
    scan(arr, n);
    int target = 0;
    for (int i = 0; i < n; i++)
        target += arr[i];
    int dp[n + 1][target + 1];
    for (int i = 0; i < n + 1; i++)
    {
        for (int j = 0; j < target + 1; j++)
        {
            if (i == 0)
                dp[i][j] = 0;
            if (j == 0)
                dp[i][j] = 1;
        }
    }
    for (int i = 1; i < n + 1; i++)
    {
        for (int j = 1; j < target + 1; j++)
        {
            if (arr[i - 1] > j)
                dp[i][j] = dp[i - 1][j];
            else
                dp[i][j] = dp[i - 1][j] || dp[i - 1][j - arr[i - 1]];
        }
    }
    int max_sum = 0;
    for (int i = 0; i <= target / 2; i++)
    {
        if (dp[n][i] == 1)
            max_sum = max(max_sum, i);
    }
    cout << target - 2 * max_sum;
    nl;
}

